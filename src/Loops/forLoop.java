package Loops;

public class forLoop {
    public static void main() {

        // for loop

        /*
         for(initialisation : condition : increment/decrement){
                    body
         }
         */

        // Helpful when the number of iterations are known

        for(int i = 0 ; i < 10 ; i++){
            System.out.println(i);
        }

    }
}
